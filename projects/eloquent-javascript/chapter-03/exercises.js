////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  // check if num1 is greater than num2
  // if so, return num2
  // if not, return num1
  if (num1 > num2) {
    return num2;
  } else {
    return num1;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num1) {
  if (num1 === 1) return false;
  else if (num1 === 0) return true;
  else if (num1 < 0) return isEven(-num1);
  else return isEven(num1 - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  // declare a count variable
  let count = 0;
  // iterate through indexes in the string
  for (let i=0; i < string.length; i++) {
  // if the character at the index is "b" or "B", add 1 to the count
    if (string[i] === char) {
      count += 1;
    } 
  }
  // return the count
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  // declare a count variable
  let count = 0;
  // iterate through indexes in the string
  for (let i=0; i < string.length; i++) {
  // if the character at the index is "b" or "B", add 1 to the count
    if (string[i] === "b" || string[i] === "B") {
      count += 1;
    } 
  }
  // return the count
  return count;
}

